#!/usr/bin/python3
""" File 0-making_change"""


def makeChange(coins, total):
    """ Generate changes needed to reach total amount """
    if total <= 0:
        return 0
    check = 0
    temp = 0
    coins.sort(reverse=True)
    for i in coins:
        while check < total:
            check += i
            temp += 1
        if check == total:
            return temp
        check -= i
        temp -= 1
    return -1
